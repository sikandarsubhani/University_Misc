.MODEL SMALL
.STACK 100H
.DATA  
ARR DB 13,10,19,14,28,29,12,24,16  
LARGEST DB 0
.CODE

MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV SI,OFFSET(ARR)   
    MOV CX,9             

L1: MOV BL,[SI]

COMP: CMP BL,LARGEST	
JGE THEN
JMP ELSE		


THEN:  MOV LARGEST,BL

ELSE:


EXIT:         INC SI
LOOP L1
    
    MOV AH,2
    MOV DL,LARGEST
    INT 21H
    MAIN ENDP
END MAIN
